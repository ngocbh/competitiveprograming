#include <bits/stdc++.h>

const long N = 1000 + 10;
using namespace std;

int n;
char a[N][N];
string s;
int ans = 0;
int main()
{  // freopen("in.inp","r",stdin);
    getline(cin,s);

    a[0][0] = 'A'; a[0][1] = 'B'; a[0][2] = 'C', a[0][3] = 'D', a[0][4] = 'E', a[0][5] = 'F' , a[0][6] = 'G';
    a[1][0] = 'H', a[1][1] = 'I', a[1][2] = 'J', a[1][3] = 'K', a[1][4] = 'L', a[1][5] = 'M' , a[1][6] = 'N';
    a[2][0] = 'O', a[2][1] = 'P', a[2][2] = 'Q', a[2][3] = 'R', a[2][4] = 'S', a[2][5] = 'T' , a[2][6] = 'U';
    a[3][0] = 'V', a[3][1] = 'W', a[3][2] = 'X', a[3][3] = 'Y', a[3][4] = 'Z', a[3][5] = '1' , a[3][6] = '2';
    a[4][0] = '3', a[4][1] = '4', a[4][2] = '5', a[4][3] = '6', a[4][4] = '7', a[4][5] = '8' , a[4][6] = '9';
    a[5][0] = '0', a[5][1] = '$', a[5][2] = '.', a[5][3] = ',', a[5][4] = '-', a[5][5] = '!' , a[5][6] = '?';
    a[6][0] = ':', a[6][1] = '/', a[6][2] = '+', a[6][3] = '(', a[6][4] = ')', a[6][5] = '*' , a[6][6] = '@';
    a[7][0] = '&', a[7][1] = '%', a[7][2] = '#', a[7][3] = '=', a[7][4] = '<', a[7][5] = '>' , a[7][6] = ' ';

    int curi = 0,curj = 0;
    for (int o = 0; o < s.size(); o++) {
        for (int i = 0; i <= 7; i++)
            for (int j = 0; j <= 7; j++)
                if ( s[o] == a[i][j] ) {
                    ans += abs(i-curi) + abs(j-curj) + 1, curi = i, curj = j;
                }
    }
    cout << ans + 1;

}
